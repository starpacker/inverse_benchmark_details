import sys
import os
import dill
import numpy as np
import traceback

# Import the target function
from agent_forward_operator import forward_operator
from verification_utils import recursive_check

def main():
    """Main test function for forward_operator."""
    
    # Data paths provided
    data_paths = ['/data/yjh/torch_radon_ct_sandbox_sandbox/run_code/std_data/standard_data_forward_operator.pkl']
    
    # Separate outer and inner paths
    outer_path = None
    inner_paths = []
    
    for path in data_paths:
        basename = os.path.basename(path)
        if 'parent_function' in basename or 'parent_' in basename:
            inner_paths.append(path)
        elif basename == 'standard_data_forward_operator.pkl':
            outer_path = path
    
    if outer_path is None:
        print("ERROR: Could not find outer data file (standard_data_forward_operator.pkl)")
        sys.exit(1)
    
    # Phase 1: Load outer data and call forward_operator
    try:
        with open(outer_path, 'rb') as f:
            outer_data = dill.load(f)
        print(f"Loaded outer data from: {outer_path}")
    except Exception as e:
        print(f"ERROR: Failed to load outer data: {e}")
        traceback.print_exc()
        sys.exit(1)
    
    # Extract args and kwargs from outer data
    outer_args = outer_data.get('args', ())
    outer_kwargs = outer_data.get('kwargs', {})
    outer_output = outer_data.get('output')
    
    print(f"Outer args count: {len(outer_args)}")
    print(f"Outer kwargs keys: {list(outer_kwargs.keys())}")
    
    # Phase 2: Execute the function
    try:
        if inner_paths:
            # Scenario B: Factory/Closure pattern
            # First call forward_operator to get the operator/closure
            agent_operator = forward_operator(*outer_args, **outer_kwargs)
            print(f"Created agent_operator: {type(agent_operator)}")
            
            if not callable(agent_operator):
                print("ERROR: forward_operator did not return a callable")
                sys.exit(1)
            
            # Load inner data and execute the operator
            inner_path = inner_paths[0]  # Use first inner path
            try:
                with open(inner_path, 'rb') as f:
                    inner_data = dill.load(f)
                print(f"Loaded inner data from: {inner_path}")
            except Exception as e:
                print(f"ERROR: Failed to load inner data: {e}")
                traceback.print_exc()
                sys.exit(1)
            
            inner_args = inner_data.get('args', ())
            inner_kwargs = inner_data.get('kwargs', {})
            expected = inner_data.get('output')
            
            # Execute the operator with inner args
            result = agent_operator(*inner_args, **inner_kwargs)
            print("Executed agent_operator with inner data")
            
        else:
            # Scenario A: Simple function call
            result = forward_operator(*outer_args, **outer_kwargs)
            expected = outer_output
            print("Executed forward_operator directly (Scenario A)")
    
    except Exception as e:
        print(f"ERROR: Failed during execution: {e}")
        traceback.print_exc()
        sys.exit(1)
    
    # Phase 3: Verification
    try:
        passed, msg = recursive_check(expected, result)
        
        if passed:
            print("TEST PASSED")
            sys.exit(0)
        else:
            print(f"TEST FAILED: {msg}")
            sys.exit(1)
    
    except Exception as e:
        print(f"ERROR: Verification failed with exception: {e}")
        traceback.print_exc()
        sys.exit(1)

if __name__ == "__main__":
    main()