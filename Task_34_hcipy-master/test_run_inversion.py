# 2. The Referee (Evaluation Logic)
# ==========================================
def evaluate_results(initial_image, final_image, strehls):
    """
    Plot and save results.
    Returns the final Strehl ratio as a quantitative metric.
    """
    try:
        plt.figure(figsize=(12, 4))
        
        vmin = -5
        vmax = 0
        
        # Helper to safely get magnitude
        def safe_log_img(img):
            # If img is a Field or object with .shaped, use it, else assume array
            data = img.shaped if hasattr(img, 'shaped') else img
            # Handle complex
            if np.iscomplexobj(data):
                data = np.abs(data)**2
            return np.log10(data.real / (data.max() + 1e-20) + 1e-10)

        plt.subplot(1, 3, 1)
        plt.imshow(safe_log_img(initial_image), 
                   origin='lower', vmin=vmin, vmax=vmax, cmap='inferno')
        plt.title("Aberrated Image (Log)")
        plt.colorbar()
        
        plt.subplot(1, 3, 2)
        plt.imshow(safe_log_img(final_image), 
                   origin='lower', vmin=vmin, vmax=vmax, cmap='inferno')
        plt.title("Corrected Image (Log)")
        plt.colorbar()
        
        plt.subplot(1, 3, 3)
        plt.plot(strehls)
        plt.title("Strehl Ratio Evolution")
        plt.xlabel("Iteration")
        plt.ylabel("Strehl Ratio")
        plt.grid(True)
        
        plt.tight_layout()
        plt.savefig("hcipy_standalone_results.png")
        print("Results saved to hcipy_standalone_results.png")
        
        # Quantitative Metric: Final Strehl
        if len(strehls) > 0:
            return strehls[-1]
        return 0.0
    except Exception as e:
        print(f"Evaluation visualization failed (non-fatal): {e}")
        # Fallback metric
        return strehls[-1] if strehls else 0.0

